    private void jAttachButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jAttachButtonActionPerformed
    JFileChooser chooser = new JFileChooser();
    chooser.showOpenDialog(null);
    File f = chooser.getSelectedFile();
    String path = f.getAbsolutePath();
    
    // Load the original image
    ImageIcon originalIcon = new ImageIcon(path);
    Image originalImage = originalIcon.getImage();

    // Resize the image to the desired dimensions
    Image resizedImage = originalImage.getScaledInstance(130, 130, Image.SCALE_SMOOTH);

    // Create a new ImageIcon from the resized image
    ImageIcon resizedIcon = new ImageIcon(resizedImage);

    jLabel9.setIcon(resizedIcon);
    }//GEN-LAST:event_jAttachButtonActionPerformed

