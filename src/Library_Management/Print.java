    private void jSearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSearchButtonActionPerformed
        // TODO add your handling code here:
        // SQL query to retrieve student information
        String sql = "select * from student where student_id=?";
        try {
            ps = con.prepareStatement(sql);
            ps.setString(1, jSearchID.getText());
            rs = ps.executeQuery();
            if (rs.next()) {
                String add6 = rs.getString("student_id");
                jTextStdID.setText(add6);

                String add8 = rs.getString("name");
                jTextName.setText(add8);

                String add9 = rs.getString("course");
                jTextCourse.setText(add9);

                String add11 = rs.getString("semister");
                jTextSemester.setText(add11);
                rs.close();
                ps.close();
            } else {
                JOptionPane.showMessageDialog(null, "Student ID not found");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                rs.close();
                ps.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }                                    
    }//GEN-LAST:event_jSearchButtonActionPerformed

